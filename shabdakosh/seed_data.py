def seed():
    conn = sqlite3.connect(DB_PATH)
    
    # Clear existing terms
    conn.execute('DELETE FROM terms')
    
    # Insert terms
    conn.executemany('''
        INSERT INTO terms (english, nepali, pronunciation, subject, grade, definition, example)
        VALUES (?, ?, ?, ?, ?, ?, ?)
    ''', TERMS)
    
    conn.commit()
    count = conn.execute('SELECT COUNT(*) FROM terms').fetchone()[0]
    print(f"✅ Seeded {count} terms into database!")
    conn.close()

